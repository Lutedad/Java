package chap_02;

public class _04_Operator4 {
    public static void main(String[] args) {
        boolean kimchi = true;
        boolean eggroll = true;
        boolean pork = true;
        System.out.println(kimchi || eggroll || pork); // || means "OR"
        System.out.println(kimchi && eggroll && pork); // && means "AND"

}
}
